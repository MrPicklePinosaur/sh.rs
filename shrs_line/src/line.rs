impl Line {
    /// Start readline and read one line of user input
    pub fn read_line(&mut self) -> String {
        let mut ctx = LineCtx::default();
        self.read_events(&mut ctx).unwrap()
    }

    fn read_events(&mut self, ctx: &mut LineCtx) -> anyhow::Result<String> {
        // ensure we are always cleaning up whenever we leave this scope
        struct CleanUp;
        impl Drop for CleanUp {
            fn drop(&mut self) {
                disable_raw_mode();
            }
        }
        let _cleanup = CleanUp;

        enable_raw_mode()?;

        self.painter.init().unwrap();

        self.painter.paint(
            &self.prompt,
            &self.menu,
            StyledBuf::new(),
            ctx.cb.cursor(),
            &self.cursor,
        )?;

        loop {
            if poll(Duration::from_millis(1000))? {
                let event = read()?;

                if let Event::Key(key_event) = event {
                    if self.keybinding.handle_key_event(key_event) {
                        break;
                    }
                }

                // handle menu events
                if self.menu.is_active() {
                    self.handle_menu_keys(ctx, event.clone())?;
                } else {
                    // TODO bit hacky bubbling up control flow from funtion
                    match ctx.mode {
                        LineMode::Insert => {
                            let should_break = self.handle_insert_keys(ctx, event)?;
                            if should_break {
                                break;
                            }
                        },
                        LineMode::Normal => {
                            let should_break = self.handle_normal_keys(ctx, event)?;
                            if should_break {
                                break;
                            }
                        },
                    }
                }

                let res = ctx.cb.slice(..).as_str().unwrap();

                // syntax highlight
                let mut styled_buf = self.highlighter.highlight(res);

                // add currently selected completion to buf
                if self.menu.is_active() {
                    if let Some(selection) = self.menu.current_selection() {
                        let trimmed_selection = &selection[ctx.current_word.len()..];
                        styled_buf.push(StyledContent::new(
                            ContentStyle {
                                foreground_color: Some(Color::Red),
                                ..Default::default()
                            },
                            trimmed_selection.to_string(),
                        ));
                    }
                }

                self.painter.paint(
                    &self.prompt,
                    &self.menu,
                    styled_buf,
                    ctx.cb.cursor(),
                    &self.cursor,
                )?;
            }
        }

        let res = ctx.cb.slice(..).as_str().unwrap().to_string();
        self.history.add(res.clone());
        Ok(res)
    }

    fn handle_menu_keys(&mut self, ctx: &mut LineCtx, event: Event) -> anyhow::Result<()> {
        match event {
            Event::Key(KeyEvent {
                code: KeyCode::Enter,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                let accepted = self.menu.accept().cloned();
                if let Some(accepted) = accepted {
                    self.accept_completion(ctx, &accepted)?;
                }
            },
            Event::Key(KeyEvent {
                code: KeyCode::Esc,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                self.menu.disactivate();
            },
            Event::Key(KeyEvent {
                code: KeyCode::Tab,
                modifiers: KeyModifiers::SHIFT,
                ..
            })
            | Event::Key(KeyEvent {
                code: KeyCode::Up,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                self.menu.previous();
            },
            Event::Key(KeyEvent {
                code: KeyCode::Tab,
                modifiers: KeyModifiers::NONE,
                ..
            })
            | Event::Key(KeyEvent {
                code: KeyCode::Down,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                self.menu.next();
            },
            _ => {},
        };
        Ok(())
    }

    fn handle_insert_keys(&mut self, ctx: &mut LineCtx, event: Event) -> anyhow::Result<bool> {
        match event {
            Event::Key(KeyEvent {
                code: KeyCode::Enter,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                self.painter.newline()?;
                return Ok(true);
            },
            Event::Key(KeyEvent {
                code: KeyCode::Tab,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                self.populate_completions(ctx)?;
                self.menu.activate();

                // if completions only has one entry, automatically select it
                let completion_len = self.menu.items().len();
                if completion_len == 1 {
                    // TODO stupid ownership stuff
                    let item = self.menu.items().get(0).map(|x| (*x).clone()).unwrap();
                    self.accept_completion(ctx, &item.0)?;
                }
            },
            Event::Key(KeyEvent {
                code: KeyCode::Left,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                if ctx.cb.cursor() > 0 {
                    ctx.cb.move_cursor(Location::Before())?;
                }
            },
            Event::Key(KeyEvent {
                code: KeyCode::Right,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                if ctx.cb.cursor() < ctx.cb.len() {
                    ctx.cb.move_cursor(Location::After())?;
                }
            },
            Event::Key(KeyEvent {
                code: KeyCode::Down,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                self.history_down(ctx)?;
            },
            Event::Key(KeyEvent {
                code: KeyCode::Up,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                self.history_up(ctx)?;
            },
            Event::Key(KeyEvent {
                code: KeyCode::Esc, ..
            }) => {
                ctx.mode = LineMode::Normal;
            },
            Event::Key(KeyEvent {
                code: KeyCode::Backspace,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                if ctx.cb.len() > 0 && ctx.cb.cursor() != 0 {
                    ctx.cb.delete(Location::Before(), Location::Cursor())?;
                }
            },
            Event::Key(KeyEvent {
                code: KeyCode::Char(c),
                ..
            }) => {
                ctx.cb.insert(Location::Cursor(), &c.to_string())?;
            },
            _ => {},
        };
        Ok(false)
    }

    fn handle_normal_keys(&mut self, ctx: &mut LineCtx, event: Event) -> anyhow::Result<bool> {
        // TODO write better system to support key combinations
        match event {
            Event::Key(KeyEvent {
                code: KeyCode::Enter,
                modifiers: KeyModifiers::NONE,
                ..
            }) => {
                self.painter.newline()?;
                return Ok(true);
            },
            Event::Key(KeyEvent {
                code: KeyCode::Esc, ..
            }) => {
                self.normal_keys.clear();
            },
            Event::Key(KeyEvent {
                code: KeyCode::Char(c),
                ..
            }) => {
                self.normal_keys.push(c);

                if let Ok(Command { repeat, action }) = Parser::new().parse(&self.normal_keys) {
                    for _ in 0..repeat {
                        // special cases (possibly consulidate with execute_vi somehow)

                        if let Ok(mode) = ctx.cb.execute_vi(action.clone()) {
                            ctx.mode = mode;
                        }
                        if let Action::Move(motion) = action {
                            action.clone();
                            match motion {
                                Motion::Up => self.history_up(ctx)?,
                                Motion::Down => self.history_down(ctx)?,
                                _ => {},
                            }
                        }
                    }

                    self.normal_keys.clear();
                }
            },
            _ => {},
        }
        Ok(false)
    }

    // recalculate the current completions
    fn populate_completions(&mut self, ctx: &mut LineCtx) -> anyhow::Result<()> {
        // TODO IFS
        let args = ctx.cb.slice(..ctx.cb.cursor()).as_str().unwrap().split(' ');
        ctx.current_word = args.clone().last().unwrap_or("").to_string();

        let comp_ctx = CompletionCtx::new(args.map(|s| s.to_owned()).collect::<Vec<_>>());

        let completions = self.completer.complete(&comp_ctx);
        let completions = completions
            .iter()
            .take(10) // TODO make this config
            .collect::<Vec<_>>();

        let menuitems = completions
            .iter()
            .map(|c| (c.display(), c.accept()))
            .collect::<Vec<_>>();
        self.menu.set_items(menuitems);

        Ok(())
    }

    // replace word at cursor with accepted word (used in automcompletion)
    fn accept_completion(&mut self, ctx: &mut LineCtx, accepted: &str) -> anyhow::Result<()> {
        // first remove current word
        // TODO could implement a delete_before
        ctx.cb
            .move_cursor(Location::Rel(-(ctx.current_word.len() as isize)))?;
        ctx.cb.delete(
            Location::Cursor(),
            Location::Rel(ctx.current_word.len() as isize),
        )?;

        // then replace with the completion word
        ctx.cb.insert(Location::Cursor(), accepted)?;

        Ok(())
    }

    fn history_up(&mut self, ctx: &mut LineCtx) -> anyhow::Result<()> {
        // TODO make this logic nicer
        ctx.history_ind = (ctx.history_ind + 1).min(self.history.len().saturating_sub(1) as i32);

        if let Some(history_item) = self.history.get(ctx.history_ind as usize) {
            ctx.cb.clear();
            ctx.cb.insert(Location::Cursor(), history_item)?;
        }
        Ok(())
    }

    fn history_down(&mut self, ctx: &mut LineCtx) -> anyhow::Result<()> {
        ctx.history_ind = (ctx.history_ind - 1).max(0);

        if let Some(history_item) = self.history.get(ctx.history_ind as usize) {
            ctx.cb.clear();
            ctx.cb.insert(Location::Cursor(), history_item)?;
        }

        Ok(())
    }
}
